package payroll.vo;

public class LeaveVo {

	Integer leave_id;
	Integer leave_avail; 
	Double leave_bal;
	public Integer getLeave_id() {
		return leave_id;
	}
	public void setLeave_id(Integer leave_id) {
		this.leave_id = leave_id;
	}
	public Integer getLeave_avail() {
		return leave_avail;
	}
	public void setLeave_avail(Integer leave_avail) {
		this.leave_avail = leave_avail;
	}
	public Double getLeave_bal() {
		return leave_bal;
	}
	public void setLeave_bal(Double leave_bal) {
		this.leave_bal = leave_bal;
	}
} 
